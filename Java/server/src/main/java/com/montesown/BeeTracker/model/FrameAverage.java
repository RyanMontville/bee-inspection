package com.montesown.BeeTracker.model;

public class FrameAverage {
    private int inspectionId;
    private int boxNumber;
    private String honey;
    private String nectar;
    private String brood;
    private String cells;
    private String combPattern;
    private String queenSpotted;

    public int getInspectionId() { return inspectionId; }

    public void setInspectionId(int inspectionId) { this.inspectionId = inspectionId; }

    public int getBoxNumber() { return boxNumber; }

    public void setBoxNumber(int boxNumber) { this.boxNumber = boxNumber; }

    public String getHoney() { return honey; }

    public void setHoney(String honey) { this.honey = honey; }

    public String getNectar() { return nectar; }

    public void setNectar(String nectar) { this.nectar = nectar; }

    public String getBrood() { return brood; }

    public void setBrood(String brood) { this.brood = brood; }

    public String getCells() { return cells; }

    public void setCells(String cells) { this.cells = cells; }

    public String getCombPattern() { return combPattern; }

    public void setCombPattern(String combPattern) { this.combPattern = combPattern; }

    public String getQueenSpotted() { return queenSpotted; }

    public void setQueenSpotted(String queenSpotted) { this.queenSpotted = queenSpotted; }

}
